document.getElementsByClassName('h')[0].innerHTML='While Loop'

//while loop
let i=0;
while(i<=10){
    console.log(i)
    document.write("<br>"+i)
i++
}

//do while loop
let k=1;
do{
    console.log(k)
    document.write("<br>" +k)
    k++;
}while(k<=15);

